#进行切分的测试

#定义列表l
l = ['小明','小红','小黑','汤姆','杰瑞']

print('l[0,3]=',l[0:3])
print('l[:3]=',l[:3])
print('l[1:3]=',l[1:3])
print('l[-2]=',l[-2])

#定义列表r，列表里的内容为0-100的递增数
#r = list(range(100))

r = list()
n = 100
while n>=0:
	r.append(n)
	n=n-1

print('r[0:3]=',r[0:3])
print('r[:3]=',r[:3])
print('r[-2]=',r[-2])
#打印前10个元素中，为2的倍数到值
print('r[:10:2]=',r[:10:2])
#打印第10个到第20个元素之间到数据
print('r[10:20]=',r[10:20])
#从头打印到尾，只打印5的倍数到值
print('r[::5]=',r[::5])

